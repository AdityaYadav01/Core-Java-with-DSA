        // 2. class - It is a name group of class and properties.
        // 3. HelloWorld - It is the name of the class as well as name of the file
        // 4. static - It is a keyword which allow us to run the class without creating an object
        // 5. void - It is a keyword used when we don't want to return anything from the method
        // 6. main - It is the name of the method.
        // 7. String[] args - It is a command-line argument of string type array.

